# import math functions
from math import *

# print number
print(2.12314)
# print operation
print((5 + 4) * 3)

# number variable
num = 888

# convert number
print(str(num) + " is my favorite number")

# absolute value
num = -5
print(abs(num))

# number to the power
print(pow(num, 2))

# get the larger number
print(max(num, -10, -3, 100))

# round number
print(round(4.50001))

# round to smaller
print(floor(4.9))

# round to bigger
print(ceil(4.2))

# get root
print(sqrt(1225))


